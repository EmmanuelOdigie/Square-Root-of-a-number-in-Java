package JavaSquareRoot; // project created in eclipse

public class SquareRoot { // public class created 'SquareRoot'

	public static void main(String[] args) {// read,runs & executes code
		double squareRoot = Math.sqrt(4); // created a double variable 'squareRoot' set it to 
		// calculate the squareroot of 4, 'Math.sqrt(4)' 
		
		int myResult = (int) squareRoot; // converted double to int
		
		System.out.println(myResult); // display result	
		

	}

}

// result:
// 2